package lec02_java_basics;

// we will learn print vs println
// println is mostly used
public class GoodEvening02 {
	public static void main(String[] args) {
		System.out.print("Good Evening! "); // we changed println to print 
		System.out.print("How are you doing? ");
		System.out.print("Welcome to Java class. ");
		System.out.print("Today is Friday.");
		// println -- print line by line
		// print -- print continuously, not line by line
		// you can make it readable by putting a space at the end
	}

}
